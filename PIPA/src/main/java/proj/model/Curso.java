package proj.model;

public class Curso {
    private int id;
    private String nomeCurso;

    public void setId(int id) {
        this.id = id;
    }
    public int getId() {
        return id;
    }
    public void setNomeCurso(String nomeCurso) {
        this.nomeCurso = nomeCurso;
    }
    public String getNomeCurso() {
        return nomeCurso;
    }
}